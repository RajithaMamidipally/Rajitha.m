package Day2;

public class TaskOnArithmeticOperators {

	public static void main(String[] args) {
	 int x = 54;
	 int y = 48;
	 
	 System.out.println(x+y);
	 System.out.println(x-y);
	 System.out.println(x*y);
	 System.out.println(x/y);
	 System.out.println(15.01f/3);
	 System.out.println(15.01/3);
	 System.out.println(x%y);
	 System.out.println(54.5f/0);
	 System.out.println(-514.5/0);
	 System.out.println(0.0/0);

	}

}

