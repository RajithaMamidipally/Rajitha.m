package Day2;

public class TaskOnIntegerTypes {

	public static void main(String[] args) {
		byte b = -128;
		System.out.println(b);
		b = 12;
		System.out.println(b);
		b = 0127;
		System.out.println(b);
		b = (byte) 128;
		System.out.println(b);
		short s = 98;
		System.out.println(s);
		s = (short)327684;
		System.out.println(s);
		int i = 258;
		System.out.println(i);
		i = (Integer)549626495;
		System.out.println(i);
		long nan = 8074817115l;
		System.out.println(nan);
		

	}

}

